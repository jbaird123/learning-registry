package routines;


public class Rejects {
	private static String rejects = "\r\n";
	private static int rejectCount = 0;

	public static String getRejects() {
		return rejects;
	}

	public static void setRejects(String rejectRecord) {
		rejects += rejectRecord + "\r\n";
		rejectCount++;
	}
	
	public static void reSetRejects() {
		rejects = "";
		rejectCount=0;
	}

	public static int getRejectCount() {
		return rejectCount;
	}
 
}
